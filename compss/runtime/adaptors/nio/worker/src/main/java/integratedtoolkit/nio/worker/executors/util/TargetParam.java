package integratedtoolkit.nio.worker.executors.util;

/**
 * Class to Wrap Target Parameter
 * 
 */
public class TargetParam {

    private Object value = null;


    public TargetParam() {
    }

    public Object getValue() {
        return this.value;
    }

    public void setValue(Object value) {
        this.value = value;
    }

}
